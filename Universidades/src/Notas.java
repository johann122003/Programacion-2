public class Notas {
    private double pgr1;
    private double pgr2;
    private double pgr3;
    //constructores

    public Notas(double pgr1, double pgr2, double pgr3) {
        this.pgr1 = pgr1;
        this.pgr2 = pgr2;
        this.pgr3 = pgr3;
    }
    //getter and setter

    public double getPgr1() {
        return pgr1;
    }

    public void setPgr1(double pgr1) {
        this.pgr1 = pgr1;
    }

    public double getPgr2() {
        return pgr2;
    }

    public void setPgr2(double pgr2) {
        this.pgr2 = pgr2;
    }

    public double getPgr3() {
        return pgr3;
    }

    public void setPgr3(double pgr3) {
        this.pgr3 = pgr3;
    }
}
