package udla.Jvasquez.accesorios2;

public class Neumatico {
    String nombre;
    int cantidad;
    double precio;
    String tipo;
    String codigo;

    Neumatico(String nombre, int cantidad, double precio, String tipo, String codigo) {
        this.nombre = nombre;
        this.cantidad = cantidad;
        this.precio = precio;
        this.tipo = tipo;
        this.codigo = codigo;
    }
}