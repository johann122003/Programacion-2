package udla.Jvasquez.accesorios2;

public class Proteccion {
    String nombre;
    int cantidad;
    double precio;
    double peso;
    String lugarDeAcc;

    Proteccion(String nombre, int cantidad, double precio, double peso, String lugarDeAcc) {
        this.nombre = nombre;
        this.cantidad = cantidad;
        this.precio = precio;
        this.peso = peso;
        this.lugarDeAcc = lugarDeAcc;
    }
}